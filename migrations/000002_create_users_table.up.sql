CREATE TABLE IF NOT EXISTS users
(
    id
    INTEGER
    GENERATED
    ALWAYS AS
    IDENTITY
    PRIMARY
    KEY,
    email
    TEXT
    NOT
    NULL
    UNIQUE,
    password
    TEXT
    NOT
    NULL,
    name
    TEXT
    NOT
    NULL,
    created_at
    TIMESTAMP
    NOT
    NULL
    DEFAULT
    NOW(),
    updated_at
    TIMESTAMP
    NOT
    NULL
    DEFAULT
    NOW(),
    deleted_at
    TIMESTAMP
);